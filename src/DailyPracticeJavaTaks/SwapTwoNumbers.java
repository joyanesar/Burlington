package DailyPracticeJavaTaks;

public class SwapTwoNumbers {
    public static void main(String[] args) {

        int x = 4;   // 6

        int y = 6;   // 4



        x = x + y ; //4 + 6 = 10
        y = x - y; // 10 - 6 = 4
        x = x - y; // 10 - 4 = 6

        System.out.println("X value is " + x);
        System.out.println("Y value is " + y);

    }
}
