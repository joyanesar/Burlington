package DailyPracticeJavaTaks;

public class division {
    public static void main(String[] args) {

        int a = 5;
        int b = 3;
        int c = a + b; //8

        int result =0;

        while(c > b){

            c = c - b ; // 5,2 ;

            result++; //2
        }

        System.out.println(result );

    }
}
